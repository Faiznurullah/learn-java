
public class switchcase {
	
	public static void main (String[] args) {
		
		var grade = 'A';
		
		switch(grade)
		{
			
			case 'A':
			System.out.println("Nilai kamu baik sekali");
			break;
			
			case 'B':
			System.out.println("Nilai kamu baik");
			break;
			
			case 'C':
			System.out.println("Nilai kamu cukup");
			break;
			
			default:
			System.out.println("Tidak ada nilai");
			break;
			
			
		}
		
	}
}

